
    public class Employee
    {
        int id;
        string name;
        double salary;
        
        public int Id
        {
            get => id;
            set => id = value;
        }
        public string Name
        {
            get => name;
            set => name = value;
        }

        public double Salary
        {
            get => salary;
            set => salary = value;
        }

    }
